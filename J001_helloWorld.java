package basico;

/**
 * Toda classe tem que ter o mesmo nome do arquivo Toda classe começa com letra
 * maiscula Todo metodo começa com letra minuscula
 * 
 * public é uma palavra chave de visibilidade, indica que a classe ou metodo são
 * publicos. static é uma palavra chave de acesso, indica que o metodo pode ser
 * acessado diretamente void é uma palavra chave de retorno, indica que o metodo
 * não tera retorno.
 * 
 * Metodo com o nome main, indica que ele sera o metodo principal da classe.
 */
public class J001_helloWorld {
    public static void main(String[] argumentos) {

        System.out.println("Hello World!");
    }
}
